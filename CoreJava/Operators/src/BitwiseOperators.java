
public class BitwiseOperators {

	public static void main(String[] args) {

		System.out.println(true & false);
		System.out.println(true | false);
		System.out.println(true ^ false);

		System.out.println(4 & 5); // 100 & 101 => 100
		System.out.println(4 | 5);
		System.out.println(4 ^ 5);

		System.out.println(~4);
		System.out.println(!true);

	}

}
